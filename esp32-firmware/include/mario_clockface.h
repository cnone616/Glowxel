// 马里奥表盘类
class MarioClockface {
private:
  MatrixPanel_I2S_DMA* matrix;
  bool marioJumping = false;
  int marioY = 42;
  int marioTargetY = 20;
  int jumpDirection = 0;
  unsigned long lastUpdate = 0;
  bool blockBouncing = false;
  int blockOffsetY = 0;
  
  // 绘制sprite辅助函数
  void drawSprite(int x, int y, const unsigned short* sprite, int w, int h) {
    for (int py = 0; py < h; py++) {
      for (int px = 0; px < w; px++) {
        uint16_t color = pgm_read_word(&sprite[py * w + px]);
        if (color != _MASK && x + px >= 0 && x + px < 64 && y + py >= 0 && y + py < 64) {
          matrix->drawPixel(x + px, y + py, color);
        }
      }
    }
  }
  
public:
  MarioClockface(MatrixPanel_I2S_DMA* disp) : matrix(disp) {}
  
  // 绘制背景
  void drawBackground() {
    matrix->fillScreen(SKY_COLOR);
    
    // 白云
    drawSprite(0, 8, CLOUD1, 13, 12);
    drawSprite(50, 16, CLOUD2, 13, 12);
    
    // 青山
    drawSprite(-6, 37, HILL, 23, 26);
    
    // 灌木丛
    drawSprite(43, 47, BUSH, 21, 9);
    
    // 地面
    for (int y = 56; y < 64; y++) {
      for (int x = 0; x < 64; x++) {
        int idx = ((y - 56) * 8 + (x % 8));
        matrix->drawPixel(x, y, pgm_read_word(&GROUND[idx]));
      }
    }
  }
  
  // 显示完整场景
  void display(int hour, int minute) {
    drawBackground();
    
    // 时间方块（带弹跳偏移）
    char hourStr[3], minStr[3];
    sprintf(hourStr, "%02d", hour);
    sprintf(minStr, "%02d", minute);
    
    int blockY = 8 + blockOffsetY;
    drawSprite(13, blockY, BLOCK, 19, 19);
    drawSprite(32, blockY, BLOCK, 19, 19);
    
    // 在方块上绘制数字（使用Adafruit GFX字体）
    matrix->setTextSize(1);
    matrix->setTextColor(0x0000);
    matrix->setCursor(16, blockY + 6);
    matrix->print(hourStr);
    matrix->setCursor(35, blockY + 6);
    matrix->print(minStr);
    
    // 马里奥
    if (marioJumping) {
      drawSprite(25, marioY, MARIO_JUMP, 17, 16);
    } else {
      drawSprite(25, marioY, MARIO_IDLE, 13, 16);
    }
  }
  
  // 跳跃动画
  void update(int hour, int minute) {
    if (marioJumping) {
      if (millis() - lastUpdate >= 50) {
        if (jumpDirection == 1) {
          marioY -= 2;
          if (marioY <= marioTargetY) {
            jumpDirection = -1;
            // 马里奥到顶部，方块开始弹跳
            blockBouncing = true;
            blockOffsetY = -3;
          }
        } else if (jumpDirection == -1) {
          marioY += 2;
          if (marioY >= 42) {
            marioY = 42;
            marioJumping = false;
            jumpDirection = 0;
          }
        }
        
        // 方块弹跳恢复
        if (blockBouncing) {
          blockOffsetY++;
          if (blockOffsetY >= 0) {
            blockOffsetY = 0;
            blockBouncing = false;
          }
        }
        
        display(hour, minute);
        lastUpdate = millis();
      }
    }
  }
  
  void jump() {
    if (!marioJumping) {
      marioJumping = true;
      jumpDirection = 1;
    }
  }
};
